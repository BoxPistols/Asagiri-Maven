/**
 * Pick a random message for the given trigger and optional wave.
 * Falls back to wave-agnostic scripts if no wave-specific match is found.
 */
export function pickIntelMessage(
  trigger: IntelScript["trigger"],
  wave?: number,
): string {
  // Try wave-specific first
  if (wave !== undefined) {
    const specific = ALL_INTEL_SCRIPTS.filter(
      (s) => s.trigger === trigger && s.wave === wave,
    );
    if (specific.length > 0) {
      const script = specific[Math.floor(Math.random() * specific.length)];
      return script.messages[Math.floor(Math.random() * script.messages.length)];
    }
  }

  // Fall back to wave-agnostic
  const generic = ALL_INTEL_SCRIPTS.filter(
    (s) => s.trigger === trigger && s.wave === undefined,
  );
  if (generic.length > 0) {
    const script = generic[Math.floor(Math.random() * generic.length)];
    return script.messages[Math.floor(Math.random() * script.messages.length)];
  }

  return "情報更新中...";
}
